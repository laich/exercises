	public class Horse{
		private String name;
		private Integer speed;
		
		public Horse(String name, Integer speed) {
			super();
			this.name = name;
			this.speed = speed;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public Integer getSpeed() {
			return speed;
		}
		public void setSpeed(Integer speed) {
			this.speed = speed;
		}
		
	}